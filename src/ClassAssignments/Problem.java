package ClassAssignments;

public class Problem {
	public static void main(String[] args) {

		int n = 6;
		// int i=0;
		for (int i = 1; i <= n; i++) {
			for (int j = 1; j <= n; j++) {

				if (j == 1 || j == n) {

					System.out.print("*");
				} else {
					System.out.print(" ");
				}
			}// System.out.println();

				System.out.println();
			}

		}
	}

